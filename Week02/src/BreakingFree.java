
public class BreakingFree {

	public static void main(String[] args) {
		
		System.out.println("Value\t\t\tEscape");
		System.out.println("newline\t\t\t\\n");
		System.out.println("horizontal tab\t\t\\t");
		System.out.println("vertical tab\t\t\\v");
		System.out.println("backspace\t\t\\b");
		System.out.println("carriage return\t\t\\r");
		System.out.println("form feed\t\t\\f");
		System.out.println("alert\t\t\t\\a");
		System.out.println("backslash\t\t\\\\");
		System.out.println("question mark\t\t\\?");
		System.out.println("single quote\t\t\\'");
		System.out.println("double quote\t\t\\\"");
		System.out.println("the null character\t\\0");
		

	}

}
